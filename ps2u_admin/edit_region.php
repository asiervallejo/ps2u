        $sqlGames="UPDATE region SET nombre=? WHERE id_region=?";

        $pre=mysqli_prepare($conexion,$sqlGames);

        if ($pre) {
            mysqli_stmt_bind_param($pre,"si",$region,$id);
            if (!mysqli_stmt_execute($pre)){
               $query_success = FALSE; 
            }
        }
        mysqli_stmt_close($pre);

        if ($query_success) {
            mysqli_commit($conexion);
            header("Location: region-list.php");
        } else {
            mysqli_rollback($conexion);
            echo '<span class="alert-danger">No se pudo actualizar</span>';
